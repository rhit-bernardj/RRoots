// Answer.java
public class Answer {
    private String artist;
    private int points;

    // Constructor
    public Answer(String artist, int points) {
        this.artist = artist;
        this.points = points;
    }

    // Getters
    public String getArtist() {
        return artist;
    }

    public int getPoints() {
        return points;
    }
}
